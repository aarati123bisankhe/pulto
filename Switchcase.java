public class Switchcase {
    public static void main(String[] args) {
        char Value = 'b';
        int intVal = 10;
        switch(Value){
            case 'a' :
               System.out.println("Apple");
               break;
            case 'b' :
               System.out.println("Ball");
               break;
            case 'c' :
               System.out.println("cat");
               break;
            default:
               System.out.println("No word found");
               break;      
        }

    }
    
}
